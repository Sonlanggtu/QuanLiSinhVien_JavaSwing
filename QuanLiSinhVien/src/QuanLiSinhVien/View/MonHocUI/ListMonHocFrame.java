    private void btnModifleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifleActionPerformed
        int row = monHocTable.getSelectedRow();
        if (row == -1)
        {
            JOptionPane.showMessageDialog(ListMonHocFrame.this, "Vui lòng chọn bản ghi trước", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } 
        else
        {
            int confirm = JOptionPane.showConfirmDialog(ListMonHocFrame.this, "Bạn có chắc chắn muốn sửa bản ghi này ?");
            if (confirm == JOptionPane.YES_OPTION) {
                String GetMaMH =  String.valueOf(monHocTable.getValueAt(row, 0));
                new EditMonHocFrame(GetMaMH).setVisible(true);
                setVisible(false);
            }
        }
    }//GEN-LAST:event_btnModifleActionPerformed

