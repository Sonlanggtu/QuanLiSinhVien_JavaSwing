    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed

        defaultTableModel.setRowCount(0);
        SetTableData(monHocService.GetAllMonHoc());
    }//GEN-LAST:event_btnRefreshActionPerformed
    
    private void SetTableData(List<MonHoc> listMonHoc)
    {
        for (MonHoc monHoc : listMonHoc) {
            defaultTableModel.addRow(new Object[]{monHoc.GetMaMH(),monHoc.GetTenMH(), monHoc.GetSoTrinh()});
        }
    }
    
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = monHocTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ListMonHocFrame.this, "Vui lòng chọn bản ghi trước", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(ListMonHocFrame.this, "Bạn có chắc chắn xóa bản ghi này ?");
            if (confirm == JOptionPane.YES_OPTION) {
                String GetMaMH =  String.valueOf(monHocTable.getValueAt(row, 0));
                monHocService.DelSinhVien(GetMaMH);

                defaultTableModel.setRowCount(0);
                SetTableData(monHocService.GetAllMonHoc());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

