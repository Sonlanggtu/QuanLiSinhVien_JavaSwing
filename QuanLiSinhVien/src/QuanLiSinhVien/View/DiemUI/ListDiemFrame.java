    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = diemTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ListDiemFrame.this, "Vui lòng chọn bản ghi trước", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(ListDiemFrame.this, "Bạn có chắc chắn xóa bản ghi này ?");
            if (confirm == JOptionPane.YES_OPTION) {
                   String GetMaSV =  String.valueOf(diemTable.getValueAt(row, 0));
                   String GetMaMH = String.valueOf(diemTable.getValueAt(row, 1));
                   String GetHocki =  String.valueOf(diemTable.getValueAt(row, 2));
                   diemService.DeleteDiem(GetMaSV, GetMaMH, Integer.valueOf(GetHocki));
                   
                   defaultTableModel.setRowCount(0);
                   SetTableData(diemService.GetAllDiem());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnModifleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifleActionPerformed
         int row = diemTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ListDiemFrame.this, "Vui lòng chọn bản ghi trước", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(ListDiemFrame.this, "Bạn có chắc chắn muốn sửa bản ghi này ?");
            if (confirm == JOptionPane.YES_OPTION) {
                   String GetMaSV =  String.valueOf(diemTable.getValueAt(row, 0));
                   String GetMaMH = String.valueOf(diemTable.getValueAt(row, 1));
                   String GetHocki =  String.valueOf(diemTable.getValueAt(row, 2));
                new EditDiemFrame(GetMaSV, GetMaMH, Integer.valueOf(GetHocki)).setVisible(true);
                setVisible(false);
            }
        }
    }//GEN-LAST:event_btnModifleActionPerformed

