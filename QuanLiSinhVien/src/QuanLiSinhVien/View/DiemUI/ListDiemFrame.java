    private void SetTableData(List<Diem> diem) {
        for (Diem diemsql : diem) {
            defaultTableModel.addRow(new Object[]{diemsql.GetMaSV(), diemsql.GetMaMH(), diemsql.GetHocKy(), diemsql.GetDiemLan1(), diemsql.GetDiemLan2()});
        }
    }

